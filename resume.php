    <script>
        // Initialize AOS Animation
        AOS.init({
            duration: 1000,
            once: true
        });

        // Navbar Scroll Effect
        window.addEventListener('scroll', () => {
            const navbar = document.querySelector('.resume-navbar');
            if (window.scrollY > 50) {
                navbar.classList.add('resume-scrolled');
            } else {
                navbar.classList.remove('resume-scrolled');
            }
        });

        // Create Floating Particles
        function createResumeParticles() {
            const container = document.getElementById('resumeParticles');
            if (!container) return;
            
            for (let i = 0; i < 35; i++) {
                const particle = document.createElement('div');
                particle.className = 'resume-particle';
                particle.style.left = Math.random() * 100 + '%';
                particle.style.top = Math.random() * 100 + '%';
                particle.style.width = Math.random() * 5 + 2 + 'px';
                particle.style.height = particle.style.width;
                particle.style.animationDelay = Math.random() * 6 + 's';
                particle.style.animationDuration = Math.random() * 6 + 4 + 's';
                container.appendChild(particle);
            }
        }
        
        createResumeParticles();
    </script>
