    <script>
        // Initialize AOS Animation
        AOS.init({
            duration: 1000,
            once: true
        });

        // Navbar Scroll Effect
        window.addEventListener('scroll', () => {
            const navbar = document.querySelector('.skills-navbar');
            if (window.scrollY > 50) {
                navbar.classList.add('skills-scrolled');
            } else {
                navbar.classList.remove('skills-scrolled');
            }
        });

        // Create Floating Particles
        function createSkillsParticles() {
            const container = document.getElementById('skillsParticles');
            if (!container) return;
            
            for (let i = 0; i < 40; i++) {
                const particle = document.createElement('div');
                particle.className = 'skills-particle';
                particle.style.left = Math.random() * 100 + '%';
                particle.style.top = Math.random() * 100 + '%';
                particle.style.width = Math.random() * 5 + 2 + 'px';
                particle.style.height = particle.style.width;
                particle.style.animationDelay = Math.random() * 6 + 's';
                particle.style.animationDuration = Math.random() * 6 + 4 + 's';
                container.appendChild(particle);
            }
        }
        
        createSkillsParticles();
    </script>
