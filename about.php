    <script>
        // Initialize AOS Animation
        AOS.init({
            duration: 1000,
            once: true
        });

        // Navbar Scroll Effect
        window.addEventListener('scroll', () => {
            const navbar = document.querySelector('.about-navbar');
            if (window.scrollY > 50) {
                navbar.classList.add('about-scrolled');
            } else {
                navbar.classList.remove('about-scrolled');
            }
        });

        // Create Floating Particles
        function createAboutParticles() {
            const container = document.getElementById('aboutParticles');
            if (!container) return;
            
            for (let i = 0; i < 40; i++) {
                const particle = document.createElement('div');
                particle.className = 'about-particle';
                particle.style.left = Math.random() * 100 + '%';
                particle.style.top = Math.random() * 100 + '%';
                particle.style.width = Math.random() * 5 + 2 + 'px';
                particle.style.height = particle.style.width;
                particle.style.animationDelay = Math.random() * 6 + 's';
                particle.style.animationDuration = Math.random() * 6 + 4 + 's';
                container.appendChild(particle);
            }
        }
        
        createAboutParticles();

        // Animate Progress Bars on Scroll
        function animateProgressBars() {
            const progressBars = document.querySelectorAll('.about-skill-progress-bar');
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const width = entry.target.style.width;
                        entry.target.style.width = '0%';
                        setTimeout(() => {
                            entry.target.style.width = width;
                        }, 200);
                        observer.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.5 });
            
            progressBars.forEach(bar => observer.observe(bar));
        }
        
        animateProgressBars();
    </script>
